package com.altimetrik.def.model;

import java.util.List;

import com.offbytwo.jenkins.model.Artifact;
import com.offbytwo.jenkins.model.BuildChangeSet;
import com.offbytwo.jenkins.model.BuildChangeSetAuthor;
import com.offbytwo.jenkins.model.BuildResult;

public class BuildDetails {
	
    private List actions; // TODO: Should be improved.
    private boolean building;
    private String description;
    private String displayName;
    private long duration;
    private long estimatedDuration;
    private String fullDisplayName;
    private String id;
    private long timestamp;
    private BuildResult result;
    private List<Artifact> artifacts;
    private String consoleOutputText;
    private String consoleOutputHtml;
    private BuildChangeSet changeSet;
    private String builtOn;
    private List<BuildChangeSetAuthor> culprits;
    
    
	public List getActions() {
		return actions;
	}
	public void setActions(List actions) {
		this.actions = actions;
	}
	public boolean isBuilding() {
		return building;
	}
	public void setBuilding(boolean building) {
		this.building = building;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDisplayName() {
		return displayName;
	}
	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}
	public long getDuration() {
		return duration;
	}
	public void setDuration(long duration) {
		this.duration = duration;
	}
	public long getEstimatedDuration() {
		return estimatedDuration;
	}
	public void setEstimatedDuration(long estimatedDuration) {
		this.estimatedDuration = estimatedDuration;
	}
	public String getFullDisplayName() {
		return fullDisplayName;
	}
	public void setFullDisplayName(String fullDisplayName) {
		this.fullDisplayName = fullDisplayName;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public long getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(long timestamp) {
		this.timestamp = timestamp;
	}
	public BuildResult getResult() {
		return result;
	}
	public void setResult(BuildResult result) {
		this.result = result;
	}
	public List<Artifact> getArtifacts() {
		return artifacts;
	}
	public void setArtifacts(List<Artifact> artifacts) {
		this.artifacts = artifacts;
	}
	public String getConsoleOutputText() {
		return consoleOutputText;
	}
	public void setConsoleOutputText(String consoleOutputText) {
		this.consoleOutputText = consoleOutputText;
	}
	public String getConsoleOutputHtml() {
		return consoleOutputHtml;
	}
	public void setConsoleOutputHtml(String consoleOutputHtml) {
		this.consoleOutputHtml = consoleOutputHtml;
	}
	public BuildChangeSet getChangeSet() {
		return changeSet;
	}
	public void setChangeSet(BuildChangeSet changeSet) {
		this.changeSet = changeSet;
	}
	public String getBuiltOn() {
		return builtOn;
	}
	public void setBuiltOn(String builtOn) {
		this.builtOn = builtOn;
	}
	public List<BuildChangeSetAuthor> getCulprits() {
		return culprits;
	}
	public void setCulprits(List<BuildChangeSetAuthor> culprits) {
		this.culprits = culprits;
	}
    
    
}
